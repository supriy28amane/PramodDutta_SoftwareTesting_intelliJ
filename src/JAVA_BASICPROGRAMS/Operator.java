package JAVA_BASICPROGRAMS;

public class Operator {
    public static void main(String[] args) {
        // Arithmetic Operators
        // +,-,%,/,*
        int a=20,b=10;;
        System.out.println("Add :"+(a+b));
        System.out.println("Subs :"+(a-b));
        System.out.println("Mul : "+(a*b));
        System.out.println("div :"+(a/b));
        System.out.println("------");

        //assignment operator
        int a1  = 10; // assign the values.
        boolean b1 = (10 == 11); // == Compare between the two values - True/False
        // === NOT PRESENT in the JAVA, === present Javascript
        System.out.println(a1);
        System.out.println(b1);
        System.out.println("------");

        // Compound Assignment Operators
        // +=, -=, /=, *=

        // Relational Operators
        // > , < , >= <=, !, != , == (compare two value)


        // Logical Operators
        //  OR or and , || , && , !

        // They will come in the future.  -OOPs concepts

        // new Operator
        // instanceOf Operator


        // Optional to learn - digital Electronics
        // Bitwise - Not important for Automation
        // ~ - Bitwise Not, >> - Right shift, << Left Shift, ^ - XO
    }
}
